<?php


namespace frontend\controllers;

use Yii;
use common\models\cms\PageForm;
use common\models\cms\PageService;
use common\models\cms\WsPage;
use yii\helpers\ArrayHelper;
use yii\web\NotFoundHttpException;

/**
 * Class CmsController
 * @package frontend\controllers
 *
 * @property WsPage $page
 */
class CmsController extends FrontendController
{

    public function init()
    {
        parent::init();
        if ($this->page === null) {
            throw new NotFoundHttpException(Yii::t('frontend', "Not found page {type}", ['type' => $this->type]));
        }
        if($this->page->title){
            $this->site_title = $this->page->title;
        }
        if($this->page->image){
            $this->site_image = $this->page->image;
        }
        if($this->page->description){
            $this->site_description = $this->page->image;
        }
    }

    const MAX_ITEM_SIZE = 4;

    public $layout = '@frontend/views/layouts/cms';

    public $type = WsPage::TYPE_HOME;

    public $isShow = false;


    public function beforeAction($action)
    {

        return parent::beforeAction($action); // TODO: Change the autogenerated stub
    }

    /**
     * @var WsPage
     */
    private $_page;

    /**
     * @return WsPage|null
     */
    public function getPage()
    {
        if ($this->_page === null) {
            $this->_page = $this->getActivePage();
        }
        return $this->_page;
    }

    protected function metaTag()
    {

        return ArrayHelper::merge(parent::ogMetaTag(), $this->page->url !== null ? [
            'url' => $this->page->url ? $this->page->url : $this->request->absoluteUrl,
        ] : []);
    }

    protected function ogMetaTag()
    {
        return ArrayHelper::merge(parent::ogMetaTag(), $this->page->url !== null ? [
            'url' => $this->page->url ? $this->page->url : $this->request->absoluteUrl,
        ] : []);
    }

    protected function linkTag()
    {
        return ArrayHelper::merge(parent::linkTag(), $this->page !== null ? [
            'canonical' => $this->page->url,
        ] : []);
    }

    /**
     * @param $page
     */
    public function setPage($page)
    {
        $this->_page = $page;
    }

    /**
     * @return WsPage|null
     */
    protected function getActivePage()
    {
        return PageService::getPage($this->type, $this->storeManager->getId());
    }

    public function defaultLayoutParams()
    {
        return array_merge(parent::defaultLayoutParams(), [
            'page' => $this->page, 'isShow' => $this->isShow
        ]);
    }

    public function defaultViewParams()
    {
        return array_merge(parent::defaultLayoutParams(), [
            ['page' => $this->page]
        ]);
    }

    /**
     * @param int $p
     * @param int $limit
     * @return array
     */
    public function renderBlock($p = 1, $limit = 6)
    {
        if ($limit > 1) {
            $offset = ($p - 1) * $limit;
        } else {
            $offset = -1;
        }
        return (new PageForm())->initBlock($this->page, $limit, $offset);
    }
}
